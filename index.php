                    <h4>Histoire du logiciel libre</h4>
                    <p>
                        À l'époque des premiers ordinateurs, c'était le matériel informatique qui était censé constituer la source de revenus, le logiciel n'étant qu'un
                        moyen d'en faciliter la vente. Les milieux professionnels et universitaires s'échangeaient volontiers logiciels et codes sources, et les 
                        constructeurs cédaient le leur pour rien jusqu'à ce que les lois antitrust ne le leur interdisent afin de permettre l'exercice d'une concurrence
                        dans ce domaine
                    </p>
                    <p>
                        Par décisions de justice, les constructeurs sont contraints de facturer séparément leurs logiciels au début des années 1970
                        ce modèle et donner un essor aux éditeurs de logiciels qui s'orientent vers la vente de licences d'utilisation.  Stallman commença à constater ces
                        restrictions. Brillant programmeur, il crée le projet <a href="https://fr.wikipedia.org/wiki/GNU">GNU</a>. Puis viendra 
                        le noyau <a href="https://fr.wikipedia.org/wiki/Noyau_Linux">Linux</a> et enfin les <a href="https://fr.wikipedia.org/wiki/Linux">Les distributions GNU/Linux</a>. 
                    </p>
