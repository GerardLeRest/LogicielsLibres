                        Un logiciel libre  est un logiciel dont l'utilisation, l'étude, la modification et la duplication par autrui en vue de sa diffusion
                        sont permises, techniquement et juridiquement1, ceci afin de garantir certaines libertés induites, dont le contrôle du programme
