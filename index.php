                    <h4>L'histoire de LibreOffice</h4>
                    <p>
                        Star Division, une entreprise allemande fondée au milieu des années 1980 crée la suite bureautique multiplateformes et multilingue <span class="fw-bold">StarOffice</span>
                        qu’elle distribue jusqu’à sa version 5.1, en 1999, année de son acquisition par la société <span class="fw-bold">Sun Microsystems</span>.
                        Sun ouvre le code sous licence GPL en octobre 2000, et la version 1.0 d’OpenOffice.org sort en 2 mai 2002.
                        Toutefois, 8 versions après la conception originale du projet, et de nombreuses fonctionnalités plus tard, la suite bureautique s’est considérablement alourdie,
                        et son interface, compliquée. C’est en partie ce qui motive la création d’alternatives telles qu’OOo4Kids et OOoLight par l’association EducOO début 2009, proposant
                        des versions simplifiées, plus rapides à charger et même, adaptées à l’enseignement !
                        Puis <span class="fw-bold">Oracle</span> rachète Sun Microsystème le 20 avril 2009, et les relations entre la firme et la communauté des développeurs d’OpenOffice se dégradent.
                        De ce mécontentement est née The Document Foundation, dont le premier projet est de maintenir le développement libre de la suite bureautique, qui trouve alors 
                        un nouveau nom le 28 septembre 2010 : LibreOffice. À la suite de ce <a href="https://fr.wikipedia.org/wiki/Fork_(d%C3%A9veloppement_logiciel)">fork</a>, Mark Shuttleworth (fondateur d'ubuntu), se prononce pour le remplacement d’OpenOffice par LibreOffice
                        dans la version d’Ubuntu en développement. Le changement aura lieu dans la version suivante d’avril 2011.
                    </p>
