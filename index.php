                    <h4>Logiciel libre et open source</h4>
                    <p>
                        Littéralement, open source signifie « code source ouvert ». Toutefois, on utilise généralement open source en référence à l’Open Source Definition
                        introduite par l’Open Source Initiative (OSI) en 1998, qui souhaitait une autre terminologie pour les logiciels libres, qui se voulait en anglais 
                        moins ambigüe et plus adaptée au monde des affaires que free software. 
                    </p>
                    <p>
                        Le mouvement pour le logiciel libre a défini des règles sur des principes éthiques, celui pour l'open source (qui en découle) a proposé une
                        traduction fonctionnelle. Cela a déclenché des différends relatifs au respect de ces principes. Les défenseurs du logiciel libre considèrent que le
                        logiciel libre est une affaire de philosophie, tandis que les partisans de l'open source rejettent toute philosophie.
                    </p>
