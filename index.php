                        0 - la liberté d'exécuter le programme, pour tous les usages ; <br />
                        1 - la liberté d'étudier le fonctionnement du programme et de l'adapter à ses besoins ; <br />
                        2 - la liberté de redistribuer des copies du programme (ce qui implique la possibilité aussi bien de donner que de vendre des copies) ; <br />
                        3 - la liberté d'améliorer le programme et de distribuer ces améliorations au public, pour en faire profiter toute la communauté. <br />
                        <a href="https://fr.wikipedia.org/wiki/Richard_Stallman">Richard Stallman</a> est à l'origine de ces quatre libertés.
